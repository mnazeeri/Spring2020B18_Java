package day02_Variables;

public class Escapte_Sequences {

    public static void main(String[] args) {
        System.out.println("Hello");
        System.out.println("\t Hello" );

        //\t means paragraph space

        System.out.println("\t\t\t Hello");
        System.out.println("cybertek School");
        System.out.println("Cybertek \nSchool");
        System.out.println("\nMy\nName\nIs\nMurtaza");
        // \n means new line (like hitting enter in a word document)

        // Print: My favorite TV show is "Games of Thrones"
        System.out.println("My favorite TV show is \"Game of Thrones\"");

        System.out.println("My faborite sport is \"Soccer\"");

        //\" This createts double quotes for words or sentences.

        System.out.println("\\");

        // "//" prints single slash on the console
        System.out.println("/");
        // "/" balck slash is only needed one to be printed

        System.out.println("My favorite book is \'Java'");

        // '\ creates single quote for words and sentences
        // In this version '\ is not needed either but for future versions

    }







}
